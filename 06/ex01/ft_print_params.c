void    ft_putstr(char *str)
{
    int i;
    i = 0;
    while(str[i])
    {
        write(1,&str[i],1);
        i++;
    }
}

int main(int a, char **b)
{
	int i;

	i = 1;
	while (b[i])	
	{
		ft_putstr(b[i]);
		write(1,"\n",1);
		i++;
	}
	return 0;
}
