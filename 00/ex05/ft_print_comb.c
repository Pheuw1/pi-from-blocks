#include <unistd.h>

void	ft_print_abc(char a, char b, char c)
{
	write(1, &a, 1);
	write(1, &b, 1);
	write(1, &c, 1);
	if (!((a == '7') && (b == '8') && (c == '9')))
	{
		write(1, ",", 1);
		write(1, " ", 1);
	}
}

void	ft_print_comb(void)
{
	char a[3];

	a[0] = '0' - 1;
	while (a[0]++ <= '9')
	{
		a[1] = a[0];
		while (a[1]++ <= '9')
		{
			a[2] = a[1];
			while (a[2]++ <= '8')
			{
				ft_print_abc(a[0], a[1], a[2]);
			}
		}
	}
}
