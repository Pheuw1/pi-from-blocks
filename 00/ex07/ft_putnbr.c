#include <unistd.h>

void	pc(char a)
{
	write(1, &a, 1);
}

void	ft_putnbr(int nbr)
{
	unsigned int	n;

	if (nbr < 0)
	{
		pc('-');
		n = nbr * -1;
	}
	else
	{
		n = nbr;
	}
	if (nbr >= 10 || nbr <= -10)
		ft_putnbr(n / 10);
	pc(n % 10 + 48);
}
