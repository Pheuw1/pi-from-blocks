int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	int i;
	int x;

	x = 0;
	while (s1[i] != 0 || s2[2] != 0 || i <= n)
	{
		x = s2[i] - s1[i];
		i++;
		if (x != 0)
			return (x);
	}
	return (x);
}
