void	ft_sort_int_tab(int *tab, int size)
{
	int i;
	int t;

	i = 0;
	while (i <= size)
	{
		if (tab[i] > tab[i + 1])
		{
			t = tab[i + 1];
			tab[i + 1] = tab[i];
			tab[i] = t;
			i = 0 - 1;
		}
		i++;
	}
}
